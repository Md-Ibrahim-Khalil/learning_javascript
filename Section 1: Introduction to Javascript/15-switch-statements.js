function day_of_week(day) {
    switch (day) {
        case 1:
            return "Friday";
        case 2:
            return "Saturday";
        case 3:
            return "Sunday";
        case 4:
            return "Monday";
        case 5:
            return "Tuesday";
        case 6:
            return "Wednesday";
        case 7:
            return "Thursday";
        default:
            return "Invalid Day";
    }
}

